from django.urls import re_path

# --------------------------------------------------
from api.views.request import (
    RequestView_asUser,
    RequestView_asAdmin,
)
from api.views.api import (
    ApiView_asUser,
    ApiView_asAdmin,
)

# =========================================================================================
#                                       CONSTANT
# =========================================================================================
app_name = "api"
# --------------------------------------------------
WORD = r"(search|id){1}"
ID = r"(id){1}"
PK = r"[A-Za-z0-9_@,\s]*"
# --------------------------------------------------


# =========================================================================================
#                                       CODE
# =========================================================================================
urlpatterns = [
    re_path(
        rf"u/request/(?P<word>{ID})/(?P<pk>{PK})",
        RequestView_asUser.as_view(),
        name="REQUEST_AS_USER",
    ),
    re_path(
        rf"a/request/(?P<word>{WORD})/(?P<pk>{PK})",
        RequestView_asAdmin.as_view(),
        name="REQUEST_AS_ADMIN",
    ),
    # --------------------------------------------------
    re_path(
        rf"u/api/(?P<word>{ID})/(?P<pk>{PK})",
        ApiView_asUser.as_view(),
        name="API_AS_USER",
    ),
    re_path(
        rf"a/api/(?P<word>{WORD})/(?P<pk>{PK})",
        ApiView_asAdmin.as_view(),
        name="API_AS_ADMIN",
    ),
    # --------------------------------------------------
]
